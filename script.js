// نمایش سریال‌ها
function displaySeries(data) {
    const seriesList = document.getElementById("series-list");
    seriesList.innerHTML = ""; // پاک کردن محتوا
    data.forEach(series => {
        const seriesDiv = document.createElement("div");
        seriesDiv.className = "series-detail";
        seriesDiv.innerHTML = `
            <h3>${series.title}</h3>
            <p><b>IMDb Code:</b> ${series.imdbCode}</p>
            <p><b>Title Type:</b> ${series.titleType}</p>
            <p><b>IMDb Votes:</b> ${series.imdbVotes}</p>
            <p><b>IMDb Rates:</b> ${series.imdbRates}</p>
            
            <!-- SoftSub Section -->
            <p class="softsub"><b>SoftSub</b></p>
            <button class="toggle-button" onclick="toggleSection('${series.title.replace(/\s/g, '')}-softsub')">نمایش فصل‌ها</button>
            <div class="season-container" id="${series.title.replace(/\s/g, '')}-softsub">
                ${series.softSub.seasons.map(season => `
                    <p>${season.name}</p>
                    <div class="link-container">
                        ${season.links.map(link => `<a href="${link.url}">${link.quality}</a>`).join('')}
                    </div>
                `).join('')}
            </div>

            <!-- Dubbed Section -->
            <p class="dubbed"><b>Dubbed</b></p>
            <button class="toggle-button" onclick="toggleSection('${series.title.replace(/\s/g, '')}-dubbed')">نمایش فصل‌ها</button>
            <div class="season-container" id="${series.title.replace(/\s/g, '')}-dubbed">
                ${series.dubbed.seasons.map(season => `
                    <p>${season.name}</p>
                    <div class="link-container">
                        ${season.links.map(link => `<a href="${link.url}">${link.quality}</a>`).join('')}
                    </div>
                `).join('')}
            </div>
        `;
        seriesList.appendChild(seriesDiv);
    });
}

